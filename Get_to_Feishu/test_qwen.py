# DashScope兼容模式的基础URL
LLM_BASE_URL = "https://dashscope.aliyuncs.com/compatible-mode/v1"

def ensure_utf8_console() -> None:
    """
    确保控制台能够处理UTF-8输出，尤其是在Windows上。
    """
    if hasattr(sys.stdout, "reconfigure"):
        sys.stdout.reconfigure(encoding="utf-8", errors="replace")
    if hasattr(sys.stderr, "reconfigure"):
        sys.stderr.reconfigure(encoding="utf-8", errors="replace")

def summarize_text(text: str) -> str:
    """
    使用通义千问模型通过DashScope的OpenAI兼容API来总结给定文本。
    """
    if not LLM_API_KEY or not text:
        print("错误: LLM_API_KEY 未设置或输入文本为空。")
        return ""

    # 兼容模式的URL需要拼接 /chat/completions
    url = f"{LLM_BASE_URL.rstrip('/')}/chat/completions"

    prompt = f"请为以下文章写一个100字左右的总结和摘要：\n\n{text}"

    body = json.dumps({
        "model": LLM_MODEL,
        "messages": [
            {"role": "system", "content": "你是一个擅长提炼文章精华的助手。"},
            {"role": "user", "content": prompt}
        ],
        "temperature": 0.7
    }, ensure_ascii=False).encode("utf-8")

    req = urllib.request.Request(
        url,
        data=body,
        headers={
            "Content-Type": "application/json; charset=utf-8",
            "Authorization": f"Bearer {LLM_API_KEY}"
        },
        method="POST",
    )

    try:
        with urllib.request.urlopen(req, timeout=30) as resp:
            if resp.status != 200:
                print(f"错误: API 请求失败，状态码 {resp.status}")
                print(f"响应: {resp.read().decode('utf-8')}")
                return ""
            resp_body = resp.read().decode("utf-8", errors="replace")
            data = json.loads(resp_body)
            # 标准OpenAI格式: data['choices'][0]['message']['content']
            summary = data.get("choices", [{}])[0].get("message", {}).get("content", "")
            return summary.strip()
    except Exception as e:
        print(f"\n在LLM摘要过程中发生错误: {e}")
        return ""

